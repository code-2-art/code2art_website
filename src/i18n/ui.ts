export type Lang = 'zh' | 'en';
type UiKeys = keyof typeof ui.zh;

/**
 * Get a translated UI string.
 * If the value is a function (e.g. count strings), returns the function itself.
 */
export function t(lang: Lang, key: UiKeys): string | ((...args: any[]) => string) {
  return ui[lang][key] ?? ui.zh[key];
}

/**
 * Build a locale-prefixed path.
 * e.g. getLocalePath('en', '/tutorials') => '/en/tutorials'
 */
export function getLocalePath(lang: Lang, path: string): string {
  const clean = path.startsWith('/') ? path : `/${path}`;
  return `/${lang}${clean}`;
}
